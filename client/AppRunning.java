    private void xemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xemActionPerformed
        requestServer r = new requestServer();
        r.request("xemApp");
        list();
        
        String data[][] = new String[datas.size()][100];
        String saveRow[][] = new String[datas.size()][100];

        for (int j = 3; j < datas.size(); j++) {
            String str = datas.get(j);
            String[] arrOfStr = str.split(" ");
            for (int i = 0; i < arrOfStr.length; i++) {
                if (arrOfStr[i].length() != 0) {
                    data[j][i] = arrOfStr[i];
                }
            }
        }
        
        int index = 0;
        for (int i = 3; i < datas.size(); i++) {
            int k = 0;
            for (int j = 0; j < data[i].length; j++) {  
                if(data[i][j] != null){
                    saveRow[index][k] = data[i][j];
                    k++;
                }
            }
            index ++;
        }
        String cols[] = {"Name App", "ID App"};
        DefaultTableModel model = new DefaultTableModel(saveRow, cols);
        list.setModel(model);
    }//GEN-LAST:event_xemActionPerformed

